package org.royaldev.royalcommands.tools;

public class Vector2D {

    private final int x, y;

    public Vector2D(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
